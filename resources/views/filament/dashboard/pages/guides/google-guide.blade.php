<div class="prose dark:prose-invert max-w-none">
    <h3>Connecting Google Business Profile</h3>
    <p>Sync your business information and manage reviews from Google Search and Maps.</p>

    <h4>Prerequisites</h4>
    <ul class="list-disc pl-5 space-y-1">
        <li>A Google Account that manages the Business Profile.</li>
        <li>The Business Profile must be <strong>verified</strong> by Google.</li>
    </ul>

    <h4>Step-by-Step</h4>
    <ol class="list-decimal pl-5 space-y-2">
        <li>
            <strong>Start Connection:</strong> Click the <span class="text-primary-600 font-medium">Connect
                Google</span> button.
        </li>
        <li>
            <strong>Google Login:</strong> Sign in with the Google Account that owns or manages the profile.
        </li>
        <li>
            <strong>Select Business:</strong> If you manage multiple businesses, you will be asked to select one (or we
            will automatically detect the best match).
        </li>
        <li>
            <strong>Permissions:</strong> Grant access to <code>business.manage</code> effectively.
        </li>
    </ol>

    <div class="bg-green-50 dark:bg-green-900/30 p-4 rounded-lg mt-4 border border-green-200 dark:border-green-700">
        <h5 class="text-green-800 dark:text-green-200 font-semibold mt-0">Verification Required</h5>
        <p class="text-sm text-green-700 dark:text-green-300 mb-0">
            Unverified locations cannot be managed via the API. Ensure you have completed the postcard or video
            verification process with Google.
        </p>
    </div>
</div>