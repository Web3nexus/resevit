<div class="prose dark:prose-invert max-w-none">
    <h3>Connecting Facebook Page</h3>
    <p>Connect your Facebook Page to manage posts, comments, and messages.</p>

    <h4>Prerequisites</h4>
    <ul class="list-disc pl-5 space-y-1">
        <li>A Facebook Account that is an Admin of the Page.</li>
    </ul>

    <h4>Step-by-Step</h4>
    <ol class="list-decimal pl-5 space-y-2">
        <li>
            <strong>Start Connection:</strong> Click the <span class="text-primary-600 font-medium">Connect
                Facebook</span> button.
        </li>
        <li>
            <strong>Facebook Login:</strong> Log in with your account.
        </li>
        <li>
            <strong>Select Page:</strong> Choose the Facebook Page you want to connect.
        </li>
        <li>
            <strong>Permissions:</strong> Grant permissions for <code>pages_show_list</code>,
            <code>pages_messaging</code>, and <code>pages_manage_metadata</code>.
        </li>
    </ol>
</div>