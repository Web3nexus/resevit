    <h3>Connecting Instagram Business</h3>
    <p>Connect your Instagram Professional account to manage DMs and comments directly.</p>

    <h4>Prerequisites</h4>
    <ul class="list-disc pl-5 space-y-1">
        <li>An <strong>Instagram Professional Account</strong> (Business or Creator).</li>
        <li>A <strong>Facebook Page</strong> connected to your Instagram account.</li>
        <li>You must be an Admin of that Facebook Page.</li>
    </ul>

    <h4>Step-by-Step</h4>
    <ol class="list-decimal pl-5 space-y-2">
        <li>
            <strong>Start Connection:</strong> Click the <span class="text-primary-600 font-medium">Connect
                Instagram</span> button.
        </li>
        <li>
            <strong>Facebook Login:</strong> Log in to the Facebook account that manages your Page.
        </li>
        <li>
            <strong>Select Instagram Account:</strong> Choose the Instagram Business account you want to connect.
        </li>
        <li>
            <strong>Select Page:</strong> Choose the Facebook Page linked to that Instagram account.
        </li>
        <li>
            <strong>Permissions:</strong> Grant permissions for <code>instagram_basic</code>,
            <code>instagram_manage_messages</code>, and <code>pages_show_list</code>.
        </li>
    </ol>

    <div class="bg-blue-50 dark:bg-blue-900/30 p-4 rounded-lg mt-4 border border-blue-200 dark:border-blue-700">
        <h5 class="text-blue-800 dark:text-blue-200 font-semibold mt-0">Tip: Check Connection</h5>
        <p class="text-sm text-blue-700 dark:text-blue-300 mb-0">
            If your Instagram account doesn't appear, go to your <strong>Facebook Page Settings > Linked
                Accounts</strong> and ensure Instagram is fully connected and reviewed.
        </p>
    </div>
</div>