    <h3>Connecting WhatsApp Business API</h3>
    <p>To connect your WhatsApp Number, you must have a <strong>Meta Business Account</strong> and a registered
        <strong>Meta App</strong>.
    </p>

    <h4>Prerequisites</h4>
    <ul class="list-disc pl-5 space-y-1">
        <li>A Meta Business Portfolio (Business Manager).</li>
        <li>A phone number <strong>not already in use</strong> on the WhatsApp mobile app (or you must delete the
            account on the mobile app first).</li>
        <li>Admin access to the Meta App linked in the Super Admin settings.</li>
    </ul>

    <h4>Step-by-Step</h4>
    <ol class="list-decimal pl-5 space-y-2">
        <li>
            <strong>Start Connection:</strong> Click the <span class="text-primary-600 font-medium">Connect
                WhatsApp</span> button on this page.
        </li>
        <li>
            <strong>Facebook Login:</strong> You will be redirected to Facebook. Log in with the account that manages
            your business.
        </li>
        <li>
            <strong>Select Business:</strong> Choose your Meta Business Portfolio.
        </li>
        <li>
            <strong>Select WhatsApp Account:</strong> Choose the WhatsApp Business Account (WABA) you want to use. If
            you don't have one, you will be prompted to create one.
        </li>
        <li>
            <strong>Verify Number:</strong> Select the phone number. You may need to verify it via SMS/Voice call if
            it's new.
        </li>
        <li>
            <strong>Permissions:</strong> Grant the <code>whatapp_business_management</code> and
            <code>whatsapp_business_messaging</code> permissions.
        </li>
    </ol>

