    <h3>Stripe Connect Keys</h3>
    <p>To enable platform payments, you need keys from your Stripe Dashboard.</p>
    <ol>
        <li>Log in to <a href="https://dashboard.stripe.com/apikeys" target="_blank"
                class="text-primary-600 underline">Stripe Dashboard</a>.</li>
        <li>Go to <strong>Developers > API keys</strong>.</li>
        <li>Copy the <strong>Publishable key</strong> (pk_...) and <strong>Secret key</strong> (sk_...).</li>
        <li>For <strong>Connect</strong>, go to <strong>Settings > Connect > Settings</strong> to find your
            <strong>Client ID</strong> (ca_...).</li>
    </ol>
</div>