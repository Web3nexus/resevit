<div class="prose dark:prose-invert">
    <h3>Google Cloud Project</h3>
    <ol>
        <li>Go to <a href="https://console.cloud.google.com/" target="_blank" class="text-primary-600 underline">Google
                Cloud Console</a>.</li>
        <li>Create a new project or select an existing one.</li>
        <li>Navigate to <strong>APIs & Services > Credentials</strong>.</li>
        <li>Click <strong>+ Create Credentials > OAuth client ID</strong>.</li>
        <li>Application Type: <strong>Web application</strong>.</li>
        <li><strong>Authorized Redirect URIs</strong>: Add the URI shown in the form field.</li>
        <li>Copy the <strong>Client ID</strong> and <strong>Client Secret</strong>.</li>
    </ol>

    <h3>Meta (Facebook) App</h3>
    <ol>
        <li>Go to <a href="https://developers.facebook.com/" target="_blank" class="text-primary-600 underline">Meta for
                Developers</a>.</li>
        <li>Create a standard Business App.</li>
        <li>Add the <strong>Facebook Login for Business</strong> product.</li>
        <li>In settings, add the Redirect URI shown in the form.</li>
        <li>Copy <strong>App ID</strong> and <strong>App Secret</strong> from Basic Settings.</li>
    </ol>
</div>